import UIKit


extension UIFont {
    class func IRANSansMobile_UltraLight(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "IRANSansMobile-UltraLight", size: size)!
    }
    
    class func IRANSansMobile(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "IRANSansMobile", size: size)!
    }
    class func IRANSansMobile_Bold(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "IRANSansMobile-Bold", size: size)!
    }
    class func IRANSansMobile_Light(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "IRANSansMobile-Light", size: size)!
    }
    class func IRANSansMobile_Medium(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "IRANSansMobile-Medium", size: size)!
    }

}
